def write_per_algorithm() -> None:
    for algo, time_unit, rows in DATA:
        path = PER / f"{algo}.csv"
        with path.open("w", newline="") as fh:
            writer = csv.writer(fh)
            writer.writerow(["ID", "CS(MB)", "CR", "SSP(%)",
                             "CT(s)", "CT(h)", "CPM(MB)",
                             "DT(s)", "DT(h)", "DPM(MB)"])
            for r in rows:
                _id, cs, cr, ssp, ct, cpm, dt, dpm = r
                if time_unit == "h":
                    ct_s, dt_s = ct * 3600, dt * 3600
                    ct_h, dt_h = ct, dt
                else:
                    ct_s, dt_s = ct, dt
                    ct_h, dt_h = ct / 3600, dt / 3600
                writer.writerow([_id,
                                 f"{cs:.3f}", f"{cr:.3f}", f"{ssp:.3f}",
                                 f"{ct_s:.3f}", f"{ct_h:.4f}", f"{cpm:.3f}",
                                 f"{dt_s:.3f}", f"{dt_h:.4f}", f"{dpm:.3f}"])


def write_overall() -> None:
    with (RESULTS / "overall.csv").open("w", newline="") as fh:
        writer = csv.writer(fh)
        writer.writerow(["Method", "AvgCR", "WAvgCR", "AvgSSP", "WAvgSSP",
                         "CRP", "TotalCT_h", "TotalDT_h",
                         "AvgCPM_GB", "AvgDPM_GB"])
        for row in OVERALL:
            writer.writerow(row)


def write_gpu() -> None:
    with (RESULTS / "gpu_memory.csv").open("w", newline="") as fh:
        writer = csv.writer(fh)
        writer.writerow(["Algorithm", "AvgGPM_MB", "Rank"])
        for algo, gpm, rank in sorted(GPU_MEM, key=lambda r: r[1]):
            writer.writerow([algo, gpm, rank if rank is not None else ""])


if __name__ == "__main__":
    write_per_algorithm()
    write_overall()
    write_gpu()
    print(f"Wrote {len(DATA)} per-algorithm CSVs into {PER}")
    print(f"Wrote overall.csv ({len(OVERALL)} rows) and gpu_memory.csv")
